void show_task(int nr,struct task_struct * p)
{
	int i,j = 4096-sizeof(struct task_struct);

  //打印进程的号和进程的状态
	printk("%d: pid=%d, state=%d, ",nr,p->pid,p->state);
	i=0;
  //进程的栈空间
	while (i<j && !((char *)(p+1))[i])
		i++;
	printk("%d (of %d) chars free in kernel stack\n\r",i,j);
}

